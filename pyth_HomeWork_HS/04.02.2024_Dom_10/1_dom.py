# 1.Фибоначи: Създайте рекурсивна функция, която изчислява n-
# тия елемент от редицата на Фибоначи.

def fib(n):
    if n <= 0:
        return "Невалиден вход. n трябва да бъде по-голямо от 0."
    elif n == 1:
        return 0
    elif n == 2:
        return 1
    else:
        return fib(n-1) + fib(n-2)

# Пример за извикване на функцията с n = 5
result = fib(5)
print(result)


