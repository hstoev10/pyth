#Напишете програма, която при въведени ден, месец и година проверява дали датата е валидна

def is_valid_date(day, month, year):
    # Проверка за валидност на месец
    if month < 1 or month > 12:
        return False

    # Проверка за валидност на година (може да се наложи допълнителна логика за години)
    if year < 1:
        return False

    # Проверка за валидност на ден в зависимост от месеца
    if month in [1, 3, 5, 7, 8, 10, 12]:
        return 1 <= day <= 31
    elif month in [4, 6, 9, 11]:
        return 1 <= day <= 30
    elif month == 2:
        if (year % 4 == 0 and year % 100 != 0) or (year % 400 == 0):
            # Високосна година - февруари има 29 дни
            return 1 <= day <= 29
        else:
            # Невисокосна година - февруари има 28 дни
            return 1 <= day <= 28
    else:
        return False

# Въвеждане на ден, месец и година от потребителя
day = int(input("Въведете ден: "))
month = int(input("Въведете месец: "))
year = int(input("Въведете година: "))

# Проверка за валидност на датата и извеждане на резултат
if is_valid_date(day, month, year):
    print("Въведените данни за дата са валидни.")
else:
    print("Въведените данни за дата не са валидни.")

