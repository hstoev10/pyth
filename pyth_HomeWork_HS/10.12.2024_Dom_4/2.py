# напишете програма, която при въведен процент правилни отговори от изпит връща оценка 
#по зададена скала: 1. 90-100%, оценка "Отличен", 2. 75-89%, оценка "Много добър", 
# 3. 60-74%, оценка "Добър", 4. 45-59%, оценка "Среден", 5. 0-44%, оценка "Слаб".  


def calculate_grade(percentage):
    if 90 <= percentage <= 100:
        return "Отличен"
    elif 75 <= percentage <= 89:
        return "Много добър"
    elif 60 <= percentage <= 74:
        return "Добър"
    elif 45 <= percentage <= 59:
        return "Среден"
    elif 0 <= percentage <= 44:
        return "Слаб"
    else:
        return "Невалиден процент"

# Въвеждане на процент правилни отговори
percentage = float(input("Въведете процент правилни отговори: "))

# Изчисляване на оценка и извеждане на резултата
grade = calculate_grade(percentage)
print(f"Оценка: {grade}")
