def main():
    # Четене на брой дни N от конзолата
    n = int(input("Въведете брой дни: "))

    # Създаване на речник за съхранение на броя на въведените дни
    days_count = {}

    # Цикъл за въвеждане на N дни
    for _ in range(n):
        # Въвеждане на ден от седмицата
        day = input("Въведете ден от седмицата: ")

        # Проверка дали въведеният текст е правилен ден от седмицата
        if is_valid_day(day):
            # Актуализиране на броя на въведения ден
            days_count[day] = days_count.get(day, 0) + 1
        else:
            print("Грешен ден от седмицата. Моля, въведете отново.")
            return  # Приключване на изпълнението в случай на грешка

    # Извеждане на резултатите
    print("Брой на въведените дни:")
    for day, count in days_count.items():
        print(f"{day}: {count} пъти")

def is_valid_day(day):
    # Проверка дали въведеният текст е правилен ден от седмицата
    valid_days = ["понеделник", "вторник", "сряда", "четвъртък", "петък", "събота", "неделя"]
    return day.lower() in valid_days

# Извикване на основната функция
main()

